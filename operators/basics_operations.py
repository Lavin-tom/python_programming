# basics operations
import math
a = 22
b = 3
print(a/b)              # 7.3333333333
print(a//b)             # 7
print(a*b)              # 66
print('a'*b)            # aaa
print(a*'b')            # bbbbbbbbbbbbbbbbbbbbb
print(a % b)              # 1
print(a ** b)           # 22^3
a += 5
print(a)                # a = a + 5  = 27
a -= 5
print(a)                # a= a - 5 = 22
c = 4.567
print(round(c))         # 5
c = -4.567
print(abs(c))           # 4.567
print(math.ceil(c))     # round to lower
print(math.floor(c))    # round to higher
print(math.factorial(b))    # factorial of b using math function
