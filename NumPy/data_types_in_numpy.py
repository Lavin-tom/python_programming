# Data types in NumPy

import numpy as np
arr = np.array([1,2,3,4,5])
print(arr.dtype)        # to check data type
print(arr.ndim)         # to check the dimention of array

arr2 = np.array(['apple','orange','grapes','lemon'])
print(arr2.dtype)

arr3 = np.array([1.4, 2.4, 3.7])
print(arr3.dtype)